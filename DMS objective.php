	<p>When was the last time that you saw a new patient and found it difficult to determine the medical
history or current therapy? The patient said he or she had not been told and did not know? There are many
instances where Electronic Health Record(EHR) would assist in transferring information to other doctors involved with
a patient's care. The EHR has been suggested as a way to help patients remember information
about their past and present health problems and to help doctors to understand the problems of a new patient.</p>

	<p>It is also helpful for the patient because it is no need to keep any information in document for future.Everything 
will be stored in EHR against his id and whenever he needs the information he will get it using that id only.</p>

	<p>The medical assistants will also be benifited from EHR because he/she will just save all the necessary information
related to a patient in a pre-defined form which will increase the working performance &amp; decrease many hestling.</p> 

<p>
