        <li>Making the health information available, reducing 
duplication of tests, reducing delays in treatment, and patients        
   well informed to take better decisions</li>
        <li>Informing, educating, and empowering people about health issues.</li>
	<li>cost savings, error reduction, and improved communication.</li>
	<li>Developing policies that support individual and community health efforts.</li>
	<li>Linking people to needed personal health services.</li>
